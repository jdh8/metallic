/* Upper limit of the result-anchored small-|x| series leg. */
#define SINH_SMALL 0.25

/* sinh(x) = x + x^3·S(x^2) for |x| < SINH_SMALL, where ½(e^x − e^-x) cancels.
 * Fast leg: plain-f64 S (degree 8, truncation ~2^-90 over the band) with an
 * x^3-scaled Ziv gate; straddle defers to the double-double series.  Port of
 * metallic-rs sinh_small. */
static double sinh_small_(double x)
{
    static const double s_fast[8] = {
        0.16666666666666666,   0.008333333333333333,  0.0001984126984126984,
        2.7557319223985893e-6, 2.505210838544172e-8,  1.6059043836821613e-10,
        7.647163731819816e-13, 2.8114572543455206e-15,
    };
    static const double s_dd[12][2] = {
        { 0.16666666666666666,    9.25185853854297e-18   },
        { 0.008333333333333333,   1.1564823173178714e-19 },
        { 0.0001984126984126984,  1.7209558293420705e-22 },
        { 2.7557319223985893e-6, -1.858393274046472e-22  },
        { 2.505210838544172e-8,  -1.448814070935912e-24  },
        { 1.6059043836821613e-10, 1.2585294588752098e-26 },
        { 7.647163731819816e-13,  7.03872877733453e-30   },
        { 2.8114572543455206e-15, 1.6508842730861433e-31 },
        { 8.22063524662433e-18,   2.2141894119604265e-34 },
        { 1.9572941063391263e-20,-1.3643503830087908e-36 },
        { 3.868170170630684e-23, -8.843177655482344e-40  },
        { 6.446950284384474e-26, -1.9330404233703465e-42 },
    };

    double v = x * x;
    double x3 = x * v;
    double s = ((((((s_fast[7] * v + s_fast[6]) * v + s_fast[5]) * v + s_fast[4]) * v
        + s_fast[3]) * v + s_fast[2]) * v + s_fast[1]) * v + s_fast[0];
    double tail = x3 * s;
    exptab_sum_ hl = exptab_fast2sum_(x, tail);   /* |x| >> |tail| */
    double err = 0x1p-49 * x3;                     /* ~26x the measured slip */
    double lo = hl.hi + (hl.lo - err);
    double hi = hl.hi + (hl.lo + err);
    if (lo == hi)
        return lo;

    /* Accurate leg: c = x^3·S(x^2) as a double-double (degree 11, truncation
     * ~2^-136), then round_anchored adds the exact x and breaks the ties. */
    exptab_sum_ vv = exptab_prod_(x, x);
    exptab_sum_ acc = { s_dd[11][0], s_dd[11][1] };
    for (int i = 10; i >= 0; --i)
        acc = exptab_add_(exptab_mul_(acc, vv), (exptab_sum_){ s_dd[i][0], s_dd[i][1] });
    exptab_sum_ c = exptab_mul_(exptab_mul_((exptab_sum_){ x, 0.0 }, vv), acc);
    return sinhtab_round_anchored_(x, c);
}

