static double sinh_wc_lookup_(double x)
{
    int lo = 0, hi = (int)(sizeof sinh_wc_ / sizeof *sinh_wc_) - 1;
    while (lo <= hi) {
        int mid = lo + (hi - lo) / 2;
        if (sinh_wc_[mid].x < x)
            lo = mid + 1;
        else if (sinh_wc_[mid].x > x)
            hi = mid - 1;
        else
            return sinh_wc_[mid].y;
    }
    return 0;
}

double sinh(double x)
{
    double s = fabs(x);

    /* ln(2·DBL_MAX) ≈ 0x1.633ce8fb9f87dp+9: above this (or NaN) propagate. */
    if (!(s <= 0x1.633ce8fb9f87dp+9))
        return x * HUGE_VAL;

    /* |x| <= 2^-26: sinh(x) rounds to exactly x (the x^3/6 term < 0.5 ulp). */
    if (s < 1.4901161193847656e-8)
        return x;

    /* |x| < ¼: ½(e^x − e^-x) cancels catastrophically, so anchor at the exact x
     * via the odd series instead. */
    if (s < SINH_SMALL)
        return copysign(sinh_small_(s), x);

    /* |x| >= ¼: sinh(x) = ½(e^x − e^-x) = 2^e · mantissa; the residual
     * cancellation is mild (≤ 1 bit), so the double-double carries it. */
    int64_t e;
    exptab_sum_ mantissa = sinhtab_combine_(s, 0, &e);

    double out;
    if (sinhtab_round_(mantissa, e, &out))
        return copysign(out, x);

    double wc = sinh_wc_lookup_(s);
    return copysign(wc ? wc : out, x);
}
