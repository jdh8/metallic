/* Restriction of (x -> erf(x) - x) to [-2, 2] */
static double kernel_(double x)
{
    const double c[] = {
        1.2837916709551257239e-01
       -3.7612638903183727743e-01,
        1.1283791670954452589e-01,
       -2.6866170645058585040e-02,
        5.2239776250282665859e-03,
       -8.5483270090933458869e-04,
        1.2055332651740064961e-04,
       -1.4925645048925617561e-05,
        1.6462052373535624492e-06,
       -1.6365306257492537811e-07,
        1.4803657738277809393e-08,
       -1.2272848617104866453e-09,
        9.3548773011507896837e-11,
       -6.5126832940882110724e-12,
        4.0229744601473001654e-13,
       -2.0557761766406878434e-14,
        7.5487360616078988699e-16,
       -1.4474500213711684009e-17
    };

    double x2 = x * x;
    double x4 = x2 * x2;
    double x8 = x4 * x4;
    double x16 = x8 * x8;

    return x * (c[0] + c[1] * x2 + (c[2] + c[3] * x2) * x4 + (c[4] + c[5] * x2 + (c[6] + c[7] * x2) * x4) * x8
        + (c[8] + c[9] * x2 + (c[10] + c[11] * x2) * x4 + (c[12] + c[13] * x2 + (c[14] + c[15] * x2) * x4)) * x8
        + (c[16] + c[17] * x2) * (x16 * x16));
}
