/* Accurate atanh for |x| <= 0.5: Taylor series with exact 128-bit coefficients.
 * atanh(x) = x * sum_{k=0}^{64} x^{2k} / (2k+1)
 * Horner: sum_k = 1/(2k+1) + x^2 * sum_{k+1}, evaluated from k=64 down to k=0. */
static inline dint_t atanh_taylor_dint_(double ax)
{
    dint_t xd = dint_from_f64_(ax);
    dint_t x2 = dint_mul_(&xd, &xd);

    dint_t sum = atanh_inv_odd_[64];  /* 1/129 */
    for (int k = 63; k >= 0; --k) {
        sum = dint_mul_(&x2, &sum);
        sum = dint_add_(&atanh_inv_odd_[k], &sum);
    }
    return dint_mul_(&xd, &sum);
}

static double atanh_accurate_(double x)
{
    double ax = fabs(x);
    dint_t result;

    if (ax <= 0.5) {
        result = atanh_taylor_dint_(ax);
    } else {
        dint_t axd = dint_from_f64_(ax);
        dint_t one = { 0, 0, (unsigned __int128)0x8000000000000000ULL << 64 };
        dint_t two = { 0, 1, (unsigned __int128)0x8000000000000000ULL << 64 };

        dint_t neg_ax = { 1, axd.ex, axd.m };
        dint_t b = dint_add_(&one, &neg_ax);  /* 1 - ax */

        double bd = dint_to_f64_(&b);
        dint_t r = dint_from_f64_(1.0 / bd);
        for (int k = 0; k < 2; ++k) {
            dint_t br = dint_mul_(&b, &r);
            dint_t neg_br = { !br.sgn, br.ex, br.m };
            dint_t t = dint_add_(&two, &neg_br);
            r = dint_mul_(&r, &t);
        }

        dint_t a = dint_add_(&one, &axd);
        dint_t ratio = dint_mul_(&a, &r);
        dint_t ln_r = dint_ln_(ratio);
        ln_r.ex -= 1;
        result = ln_r;
    }

    return copysign(dint_to_f64_(&result), x);
}

double atanh(double x)
{
    if (x != x)
        return x;

    double ax = fabs(x);

    if (ax > 1.0)
        return (x - x) / (x - x);  /* NaN, domain error */
    if (ax == 1.0)
        return copysign(INFINITY, x);

    /* |x| < 2^-27: atanh(x) = x, error < x^3/3 < 0.5 ulp(x). */
    if (ax < 0x1p-27)
        return x;

    /* u = 2*ax/(1-ax) as a double-double (u_hi, u_lo). */
    double den = 1.0 - ax;
    double two_ax = 2.0 * ax;  /* exact */
    double u_hi = two_ax / den;
    double u_lo = (two_ax - u_hi * den) / den;

    exptab_sum_ r;

    if (u_hi <= 0x1p-8) {
        /* Small u: logtab_ln1p_kernel_ is accurate for |r| <= 1/256. */
        exptab_sum_ u = { u_hi, u_lo };
        r = logtab_ln1p_kernel_(u);
    } else {
        /* Compute arg = 1 + u as a double-double.
         * For u_hi large (>> 1), 1 is negligible: arg_hi = u_hi, arg_lo = u_lo + 1 - arg_hi.
         * Use two-sum to be correct in all cases. */
        double arg_hi = 1.0 + u_hi;
        double arg_lo_s = arg_hi - 1.0;   /* rounding error from 1 + u_hi */
        double arg_lo = (u_hi - arg_lo_s) + u_lo;
        /* Now arg_hi + arg_lo ≈ 1 + u = (1+ax)/(1-ax) to ~106 bits. */

        /* ln(arg_hi + arg_lo) ≈ logtab_ln_dd_(arg_hi) + arg_lo/arg_hi */
        r = logtab_ln_dd_(arg_hi);
        r.lo += arg_lo / arg_hi;
    }

    /* Halve: atanh(x) = (1/2) * ln1p(u) */
    r.hi *= 0.5;
    r.lo *= 0.5;

    double err   = ATANH_ERR(r.hi);
    double left  = r.hi + (r.lo - err);
    double right = r.hi + (r.lo + err);

    if (left != right)
        return atanh_accurate_(x);

    return copysign(left, x);
}
