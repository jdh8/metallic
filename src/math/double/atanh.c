/* Correctly-rounded double atanh.
 *
 * atanh(x) = (1/2) * ln((1+x)/(1-x)) = (1/2) * ln1p(u)
 *   where u = 2x/(1-x).
 *
 * Fast path:
 *   u = 2x/(1-x) as a double-double (u_hi, u_lo).
 *   Form arg = 1 + u as a double-double via two-sum, then:
 *   - For |u| <= 1/256: logtab_ln1p_kernel_((u_hi, u_lo)).
 *   - Otherwise: logtab_ln_dd_(arg_hi) + arg_lo/arg_hi (first-order Taylor).
 *   Halve the result.
 *
 * Accurate path (Ziv fallback):
 *   For |x| <= 0.5: dint Taylor series with exact 128-bit coefficients:
 *     atanh(x) = x*(1/1 + x^2/3 + x^4/5 + ... + x^{128}/129)
 *   For |x| > 0.5: dint_ln_((1+|x|)/(1-|x|)) / 2 via Newton reciprocal.
 *
 * Special cases: atanh(±1)=±∞, |x|>1=NaN, atanh(0)=0, atanh(NaN)=NaN.
 */

