/* Decompose a finite positive x into binade exponent e, table index i, and
 * reduced argument r = m * INV[i] − 1 ∈ [−1/256, 1/256] as double-double. */
static inline void logtab_reduce_(double x, int64_t *e_out, int *i_out, exptab_sum_ *r_out)
{
    int bias = 0;

    if (x < 0x1p-1022) {           /* normalize subnormals */
        x *= 0x1p54;
        bias = -54;
    }

    int64_t bits = reinterpret(int64_t, x);
    *e_out = ((bits >> 52) & 0x7FF) - 1023 + bias;
    *i_out = (int)((bits >> (52 - 7)) & 127);
    double m = reinterpret(double, (bits & 0x000FFFFFFFFFFFFFLL) | 0x3FF0000000000000LL);

    /* r = m * inv − 1 as double-double; m*inv ∈ [0.996, 1.004] → hi−1 exact. */
    exptab_sum_ mi = exptab_prod_(m, logtab_inv_[*i_out]);
    *r_out = exptab_fast2sum_(mi.hi - 1.0, mi.lo);
}

/* ln(1 + r) = r · P(r) as double-double for |r| ≤ 1/256 */
static inline exptab_sum_ logtab_ln1p_kernel_(exptab_sum_ r)
{
    exptab_sum_ p = { logtab_p_[12][0], logtab_p_[12][1] };

    /* Horner in the FULL double-double r (matches metallic-rs ln_1p_kernel).
     * Using only r.hi here drops the higher-order r.lo terms, which wrecks the
     * L[i] + ln(1+r) cancellation when the result is tiny (x near 1). */
    for (int k = 11; k >= 0; --k)
        p = exptab_add_(exptab_mul_(p, r), (exptab_sum_){ logtab_p_[k][0], logtab_p_[k][1] });

    return exptab_mul_(p, r);  /* ln(1+r) = r · P(r) */
}

/* ln(x) as double-double for finite positive x ≠ 1 */
static inline exptab_sum_ logtab_ln_dd_(double x)
{
    int64_t e;
    int i;
    exptab_sum_ r;
    logtab_reduce_(x, &e, &i, &r);

    double ef = (double)e;
    exptab_sum_ e_ln2 = { ef * logtab_ln2_hi_, ef * logtab_ln2_lo_ };
    exptab_sum_ L = { logtab_l_[i][0], logtab_l_[i][1] };
    return exptab_add_(exptab_add_(e_ln2, L), logtab_ln1p_kernel_(r));
}

