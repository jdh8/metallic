#define METALLIC_KERNEL_EXP2F_H

/* Restriction of (x -> 2^x - 1) to [-0.5, 0.5] */
static double kernel_exp2f_(double x)
{
    const double c[] = {
        0.6931471805599497,
        0.2402265069590928,
        0.05550410866446075,
        0.00961812910792214,
        0.0013333558228326226,
        0.00015403530062159954,
        0.000015252658229941417,
        0.000001321562299026546,
        0.0000001020851913096976,
        7.043007099737507e-9
    };

    return x * (((((((((c[9] * x + c[8]) * x + c[7]) * x + c[6]) * x + c[5]) * x + c[4]) * x + c[3]) * x + c[2]) * x + c[1]) * x + c[0]);
}
#endif
